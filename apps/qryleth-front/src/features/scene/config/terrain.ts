/**
 * Глобальные настройки для работы с террейном и картами высот (heightmap).
 *
 * Изменяя эти константы, можно централизованно управлять ограничениями
 * без правок по всему коду.
 */

/**
 * Максимальный размер по большей стороне для PNG heightmap после нормализации.
 *
 * При загрузке и при «ленивой» миграции исходное изображение масштабируется так,
 * чтобы его большая сторона не превышала это значение (с сохранением пропорций).
 * Результирующий массив высот имеет размеры, соответствующие масштабированному
 * изображению. Значение по умолчанию — 200.
 */
export const HEIGHTMAP_MAX_DIMENSION = 200;

/**
 * Верхняя граница на количество сегментов геометрии террейна по каждой оси.
 *
 * Используется при построении геометрии (PlaneGeometry) для ограничения количества
 * сегментов, исходя из размеров мира. Значение по умолчанию — 200.
 */
export const TERRAIN_MAX_SEGMENTS = 400;
