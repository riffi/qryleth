/**
 * Типы данных для шаблонов скриптов панели скриптинга.
 * 
 * TemplateData — отдельный шаблон с кодом и кратким описанием.
 * TemplateGroup — группа шаблонов, объединённых смыслом (для левой навигации).
 */
export interface TemplateData {
  /**
   * Уникальный идентификатор шаблона (латиница/кебаб-кейс),
   * используется внутри приложения для выбора и аналитики.
   */
  id: string
  /** Человекочитаемое имя шаблона (отображается в UI). */
  name: string
  /** Краткое описание назначения шаблона (для превью в списке). */
  description: string
  /** Полный код скрипта, который будет вставлен в редактор. */
  code: string
}

export interface TemplateGroup {
  /** Уникальный идентификатор группы (латиница/кебаб-кейс). */
  id: string
  /** Название группы (отображается в левой колонке). */
  title: string
  /** Набор шаблонов в группе. */
  templates: TemplateData[]
}

