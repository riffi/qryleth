/**
 * 2D типы для плоскости XZ (вид сверху)
 *
 * Принятые оси:
 * - x: горизонталь (влево/вправо)
 * - z: глубина/дальность (ближе/дальше)
 *
 * Важно: для прямоугольника размер по оси Z называется `depth`, а не `height`,
 * чтобы избежать смысловых коллизий с 3D «высотой» по оси Y.
 */

/**
 * Точка в плоскости XZ.
 * Кортеж из двух чисел: [x, z]
 */
export type Point2 = [number, number]

/**
 * Размер в плоскости XZ: ширина и глубина.
 */
export interface Size2D {
  /** Ширина по оси X */
  width: number
  /** Глубина по оси Z (не вертикальная высота!) */
  depth: number
}

/**
 * Прямоугольник в плоскости XZ, заданный левым‑нижним углом и размерами.
 * Обратите внимание: размер по Z называется `depth`.
 */
export interface Rect2D extends Size2D {
  /** Левая граница по X */
  x: number
  /** Нижняя граница по Z */
  z: number
}

/**
 * Окружность (круг) в плоскости XZ.
 */
export interface Circle2D {
  /** Центр по X */
  x: number
  /** Центр по Z */
  z: number
  /** Радиус круга */
  radius: number
}

/**
 * Прямоугольник в форме границ (min/max) на плоскости XZ.
 * Удобен для проверок попадания точки и генерации случайных точек внутри.
 */
export interface BoundRect2D {
  /** Левая граница по X */
  minX: number
  /** Правая граница по X */
  maxX: number
  /** Нижняя граница по Z */
  minZ: number
  /** Верхняя граница по Z */
  maxZ: number
}
